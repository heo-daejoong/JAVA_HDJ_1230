package day01;

// 한줄 주석. //가 나온 뒤부터 주석.
/* 여러줄 주석. 이 사이에 있는 모든 글들이 주석.*/
public class Ex01_HelloWorld {

	/**
	 * 메소드를 설명할 때 사용하는 주석
	 * @param args
	 */
	// 프로그램이 실행되려면 main이 있어야 한다.
	public static void main(String[] args) {
			// 들여쓰기는 Tab
			// 내어쓰기는 Shift + Tab
		System.out.println("Hello World!"); // Hello World! 출력
	}

}
